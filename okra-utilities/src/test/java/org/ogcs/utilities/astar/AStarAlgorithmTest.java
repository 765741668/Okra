    public LinkedList<Point> testFindPath(int[][] blocks) {
        return AStarAlgorithm.find(5, 3, 13, 15, blocks, false);
    }

    private void init() {
        int[][] blocks = testInitBlocks();
        int flag = -1;
        try {
            LinkedList<Point> points = testFindPath(blocks);
            for (Point point : points) {
                blocks[point.getX()][point.getY()] = 2;
            }
            flag = points.isEmpty() ? 0 : 2;
        } catch (Exception e) {
            flag = -1;
        }
        if (flag == -1) {
            System.out.println("传输数据有误！");
        } else if (flag == 0) {
            System.out.println("没找到！");
        } else {
            for (int x = 0; x < blocks.length; x++) {
                for (int y = 0; y < blocks[0].length; y++) {
                    if (blocks[x][y] == 1) {
                        System.out.print("　");
                    } else if (blocks[x][y] == 0) {
                        System.out.print("〓");
                    } else if (blocks[x][y] == 2) {//输出搜索路径
                        System.out.print("※");
                    }
                }
                System.out.println();
            }
        }
    }

